package refund;

public class RefundDataBean {
	private int ref_num;
	private int lec_num;
	private String id;
	private String reg_date;
	private int sta;
	private String fin_date;
	public int getRef_num() {
		return ref_num;
	}
	public void setRef_num(int ref_num) {
		this.ref_num = ref_num;
	}
	public int getLec_num() {
		return lec_num;
	}
	public void setLec_num(int lec_num) {
		this.lec_num = lec_num;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getReg_date() {
		return reg_date;
	}
	public void setReg_date(String reg_date) {
		this.reg_date = reg_date;
	}
	public int getSta() {
		return sta;
	}
	public void setSta(int sta) {
		this.sta = sta;
	}
	public String getFin_date() {
		return fin_date;
	}
	public void setFin_date(String fin_date) {
		this.fin_date = fin_date;
	}
	
	
}
